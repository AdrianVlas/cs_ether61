void setTSBigCountObject(void) {
//записать к-во обектов
  int cntObj = current_config.n_ts;    //Кількість блоків сигналізацій
  if(cntObj<0) return;
  if(cntObj>TOTAL_OBJ) return;
  tsbigcomponent->countObject = cntObj;
}//
